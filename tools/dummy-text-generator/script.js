const textSource = document.getElementById('textSource');
const customInputWrapper = document.getElementById('customInputWrapper');
const generateMode = document.getElementById('generateMode');
const countLabel = document.getElementById('countLabel');

textSource.addEventListener('change', (e) => {
    if (e.target.value === 'custom') {
        customInputWrapper.classList.remove('hidden');
    } else {
        customInputWrapper.classList.add('hidden');
    }
});

generateMode.addEventListener('change', (e) => {
    countLabel.textContent = e.target.value === 'length' ? '文字数' : '繰り返し回数';
});

function generateText() {
    const sourceMode = textSource.value;
    const mode = generateMode.value;
    const count = parseInt(document.getElementById('countValue').value, 10);

    if (isNaN(count) || count <= 0) return;

    let baseString = "";
    if (sourceMode === 'custom') {
        baseString = document.getElementById('customString').value;
        if (!baseString) baseString = PRESET_TEXTS.ihatov;
    } else {
        baseString = PRESET_TEXTS[sourceMode];
    }

    let result = '';

    if (mode === 'length') {
        while (result.length < count) {
            result += baseString;
        }
        result = result.substring(0, count);
    } else if (mode === 'repeat') {
        result = baseString.repeat(count);
    }

    document.getElementById('outputText').value = result;
    document.getElementById('copyBtn').textContent = 'クリップボードにコピー';
}

function copyToClipboard() {
    const outputArea = document.getElementById('outputText');
    if (!outputArea.value) return;

    outputArea.select();
    navigator.clipboard.writeText(outputArea.value).then(() => {
        const copyBtn = document.getElementById('copyBtn');
        copyBtn.textContent = 'コピーしました！';
        setTimeout(() => {
            copyBtn.textContent = 'クリップボードにコピー';
        }, 2000);
    }).catch(err => {
        console.error('コピーに失敗しました', err);
    });
}

// 初期化時に一度生成しておく
generateText();